public class UnaryOperators {
  public static void main(String []args){
    
    int a = 12;
    int b = 24;

    System.out.println("a is Increament by 1: "+(a++));
    System.out.println("b is Increament by 1: "+(++b));
    System.out.println("a is decreament by 1: "+(a--));
    System.out.println("b is decreament by 1: "+(--b));
    System.out.println("The value of a is: "+(a));
    System.out.println("The value of b is: "+(b));
  }
}
